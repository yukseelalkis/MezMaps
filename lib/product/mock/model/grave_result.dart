final class GraveResult {
  final String name;
  final String surname;
  final int age;
  final String burialPlace;
  final String deathDate;
  final String burialDate;
  final String city;
  final String district;
  final String cemeteriesName;

  const GraveResult({
    required this.name,
    required this.surname,
    required this.age,
    required this.burialPlace,
    required this.deathDate,
    required this.burialDate,
    required this.city,
    required this.district,
    required this.cemeteriesName,
  });
}
